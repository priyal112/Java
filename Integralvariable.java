public class Integralvariable {
    public static void main(String[] args) {
        byte age = 24;
        short age1 = 54;
        int age2 = 34;
        long age3 = 56;

        System.out.println(age);
        System.out.println(age1);
        System.out.println(age2);
        System.out.println(age3);
    }
}