public class ArithmeticOperators {
    public static void main(String[] args) {
        int a = 1000;
        int b = 500;

        int c = a + b;
        System.out.println(c);

        int d = a - b;
        System.out.println(d);

        int x = a * b;
        System.out.println(x);

        int y = a / b;
        System.out.println(y);

        int z = a % b;
        System.out.println(z);
    }
}
