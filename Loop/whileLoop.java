package Loop;

public class whileLoop {
    public static void main(String[] args) {
        int i = 0;
        while(i<10){
            System.out.println("hello world");
            i = i + 1;
        }

        int j = 1;
        while(j <= 50){
            System.out.println(j);
            j++;
        }
    }
}
